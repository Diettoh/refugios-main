normalized AS (
  SELECT
    s.*,
    ('ASSET_PDF_RESERVAS_2025'
     || ' | key=' || s.seed_key
     || ' | src=' || s.source_file
     || ' | pax=' || s.pax
    ) AS reservation_note
  FROM seed s
),
ins_guests AS (
  INSERT INTO guests (full_name, notes)
  SELECT DISTINCT n.guest_name, 'ASSET_PDF_RESERVAS_2025'
  FROM normalized n
  WHERE NOT EXISTS (
    SELECT 1 FROM guests g WHERE LOWER(g.full_name) = LOWER(n.guest_name)
  )
  RETURNING id
),
resolved AS (
  SELECT
    n.*,
    (
      SELECT g.id
      FROM guests g
      WHERE LOWER(g.full_name) = LOWER(n.guest_name)
      ORDER BY g.id
      LIMIT 1
    ) AS guest_id
  FROM normalized n
),
ins_reservations AS (
  INSERT INTO reservations (
    guest_id,
    cabin_id,
    source,
    payment_method,
    status,
    lead_stage,
    check_in,
    check_out,
    guests_count,
    total_amount,
    notes
  )
  SELECT
    r.guest_id,
    NULL,
    'other',
    'transfer',
    'confirmed',
    'confirmed',
    r.check_in_date,
    r.check_out_date,
    GREATEST(r.pax, 1),
    0,
    r.reservation_note
  FROM resolved r
  WHERE r.guest_id IS NOT NULL
  AND NOT EXISTS (
    SELECT 1 FROM reservations rr WHERE COALESCE(rr.notes, '') = r.reservation_note
  )
  RETURNING id
)
