      SELECT g.id
      FROM guests g
      WHERE LOWER(g.full_name) = LOWER(s.full_name)
      ORDER BY g.id
      LIMIT 1
    ) AS guest_id
  FROM seed s
)
INSERT INTO reservations (
  guest_id, cabin_id, source, payment_method, status, check_in, check_out, guests_count, total_amount, notes
)
SELECT
  n.guest_id, NULL, 'other', 'transfer', 'completed', n.check_in, n.check_out, n.guests_count, 0, n.seed_note
FROM normalized n
WHERE n.guest_id IS NOT NULL
  AND NOT EXISTS (
    SELECT 1
    FROM reservations r
    WHERE r.guest_id = n.guest_id
      AND r.check_in = n.check_in
      AND r.check_out = n.check_out
      AND COALESCE(r.notes, '') = n.seed_note
  );
